            element.innerText = '提交中...';
            _component.request({
                url: xhr.url,
                method: xhr.method,
                data: formdata,
                callback: (response) => {
                    if (typeof xhr.callback == 'function') {
                        xhr.callback(response);
                        return;
                    }
                    if (!response) return;
                    if (response.code === 0) {
                        window.location.reload();
                    } else {
                        _component.alert(response.message, 3, function () {
                            element.removeAttribute('disabled');
                            element.innerText = '提交';
                        });
                    }
                }
            });
